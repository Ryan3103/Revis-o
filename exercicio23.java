package Ex_Aritmeticas.aritimeticos;

public class exercicio23 {

    public class MultiplicarElementosLista {
        public static void main(String[] args) {
            int[] numeros = {1, 2, 3, 4, 5};
            int multiplicador = 2;
            for (int num : numeros) {
                System.out.println(num * multiplicador);
            }
        }
    }
}
